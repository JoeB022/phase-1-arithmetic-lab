// 1. Create a variable called multiply set to an equation that multiplies num1 and num2
const num1 = 31;
const num2 = 2;
const multiply = num1 * num2; // 31 * 2 = 62

// 2. Create a variable called random that generates a random integer greater than 0
const random = Math.floor(Math.random() * 100) + 1; // Generates a random integer greater than 0

// 3. Create a variable called mod set to an equation that calculates the remainder of num3 / num4
const num3 = 16;
const num4 = 6;
const mod = num3 % num4; // 16 % 6 = 4 (remainder)

// 4. Create a variable called max that finds the highest number in a set, value should be 20
const max = Math.max(1, 5, 10, 20); // Finds the maximum number, which is 20

// Example usage (console logging the results):
console.log(multiply); // Output: 62
console.log(random);   // Output: a random integer greater than 0
console.log(mod);      // Output: 4
console.log(max);      // Output: 20
